#include "ft_printf.h"

void	handle_d(int d, size_t *co)
{
	if (d == -2147483648)
	{
		write(1, "-2147483648", 11);
		(*co) += 11;
		return ;
	}
	else if (d < 0)
	{
		handle_c('-', co);
		d = -d;
	}
	if (d > 9)
	{
		handle_d (d / 10, co);
	}
	d = d % 10 + 48;
	handle_c(d, co);
}
